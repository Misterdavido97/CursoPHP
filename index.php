                    <h2>¿Dónde se paga el gas más caro o más barato en Colombia?</h2><br>
                    <figure>
                    <img src="imagenes/gaslocal.jpeg"/>
                    </figure>
                    <p>Pese a que a mayor distancia entre los campos de producción y los centros
                       de consumo los usuarios de una población pagan un mayor valor por cada
                       metro cúbico de gas que consumen, el hecho de que un consumidor de Mocoa 
                       hoy esté pagando un 91 por ciento más que uno que vive en Montería llevó 
                       a las autoridades de control y vigilancia a fijar su atención en la forma 
                       en que los diferentes actores de la cadena les están cobrando a los hogares.</p><br>
                    <p>En efecto, tras analizar el comportamiento del valor que se paga en cada 
                       cabecera municipal por este energético, la Superintendencia de Servicios
                       Públicos anunció que extremará la vigilancia a la manera como se están 
                       liquidando estos costos cada mes.</p><br>
                   <p>Un documento de la entidad, que conoció EL TIEMPO, concluyó que durante el 
                      primer semestre del 2017 las ciudades más caras en esta materia fueron las
                      que se abastecen de gas natural comprimido, es decir, aquel que se lleva en
                      carrotanque para un almacenamiento y distribución local, y no está conectado
                      al Sistema de Transporte Nacional (STN).</p><br>
                    <p>Y estos mercados corresponden a las ciudades más apartadas del país, como
                      Mocoa, Pasto, San José del Guaviare y Florencia, en las que en promedio el 
                      metro cúbico entre enero y julio fue de 2.804 pesos, 2.627 pesos, 2.522 pesos
                      y 2.304 pesos, respectivamente.</p><br>
                    <p>Si bien es claro que el hecho de no estar conectadas al STN les implica 
                         de entrada un mayor costo operativo y logístico, el superintendente 
                         Delegado para Energía y Gas, José Plata Puyana, advirtió que no es clara 
                         la forma como se están aplicando estos rubros a los usuarios en sus facturas,
                         y por ello allí se enfocarán los análisis en la función de vigilancia.</p><br>
                    <p>“En lo que queda del año debemos poner la lupa al transporte terrestre del gas 
                       comprimido, en cómo se le está trasladando eso al usuario, si las empresas
                       debidamente tienen soportada toda la facturación y si lo que le están 
                       trasladando al usuario efectivamente corresponde a los costos que tiene 
                       que asumir para ese transporte”, agrega.</p><br>
                    <p>Y es que en este aspecto el funcionario revela que no es claro el criterio
                       de costos eficientes que promulga la Comisión de Regulación de Energía y 
                       Gas (Creg), ya que toda la política del Ministerio de Transporte para regular
                       fletes no aplica acá. “No está regulada esta tarifa de transporte y ni siquiera
                       hay un costo para esa tarifa”, recalca.</p><br>
                    <p>Esto ha llevado a que, contra el reclamo de la costa Atlántica por pagar el gas
                       más caro del país (reproche de los industriales en la época del Niño y por
                       la declinación de los campos de La Guajira), es en las ciudades más alejadas
                      donde están las tarifas más caras.</p><br>
